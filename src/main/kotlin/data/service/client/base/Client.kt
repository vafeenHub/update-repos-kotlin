package data.service.client.base

/**
 * Абстрактный клиент, определяющий базовый контракт для управления жизненным циклом соединений.
 *
 * Этот интерфейс предоставляет единый метод для корректного освобождения ресурсов,
 * используемых сетевыми клиентами, такими как OkHttpClient или Ktor.
 */
interface Client {
    /**
     * Закрывает соединение и освобождает все связанные ресурсы.
     *
     * Реализации этого метода должны обеспечивать корректное завершение работы
     * нижележащего HTTP-клиента, включая закрытие пулов потоков, соединений и кэшей.
     */
    fun closeConnection()
}